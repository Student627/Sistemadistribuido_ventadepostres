    public void modifica(int id,String nombre, String direccion, String telefono, String rfc){
        SetGet_Empleados vo = new SetGet_Empleados();
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        
        vo.setIdEmpleado(id);
        vo.setNombre(nombre);
        vo.setDireccion(direccion);
        vo.setTelefono(telefono);
        vo.setRFC(rfc);
        
        cc.modificaEmpleado(vo);
    }

