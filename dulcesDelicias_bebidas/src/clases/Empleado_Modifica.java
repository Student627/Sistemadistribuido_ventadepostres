        codigo = (int)tabla.getValueAt(clic, 0);
        String nombre = ""+tabla.getValueAt(clic, 1);
        String direccion = ""+tabla.getValueAt(clic, 2);
        String telefono = ""+tabla.getValueAt(clic, 3);
        String rfc = ""+tabla.getValueAt(clic, 4);

        txtid.setText(String.valueOf(codigo));
        txtnom.setText(nombre);
        txtdirec.setText(direccion);
        txtcel.setText(telefono);
        txtrfc.setText(rfc);
       
