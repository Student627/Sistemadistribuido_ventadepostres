    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        String idproducto = "", idd = txtbuscar.getText();
        Pattern pat = Pattern.compile("[0-9]");
        Matcher mat = pat.matcher(txtbuscar.getText());
        if (mat.matches()) {
            try {
                conectar cc = new conectar();
                Connection cn = cc.conexion();
                PreparedStatement pst = cn.prepareStatement("SELECT * FROM productos where idProducto=?");
                pst.setString(1, txtbuscar.getText());
                ResultSet r = pst.executeQuery();
                if (r.next()) {
                    idproducto = r.getString("idProducto");
                } else {

                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Error en la conexion de base de datos");
            }
            if (!idproducto.equals(idd)) {
                JOptionPane.showMessageDialog(null, "Error la id del producto no esta registrada");
            } else {
                if (idd.equals("")) {
                    JOptionPane.showMessageDialog(null, "Espacio en blanco");
                    // }else{ 
                    //this.agregar(id, nombre, precio, categoria, fecha, imagen);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "En el campo id solo numeros por favor");
        }
    }//GEN-LAST:event_buscarActionPerformed


